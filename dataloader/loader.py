import logging
import os
import math
import torch
from torch.utils.data import Dataset


def read_file(_path, delim="\t"):
    data = []
    if delim == "tab":
        delim = "\t"
    elif delim == "space":
        delim = " "
    with open(_path, "r") as f:
        for line in f:
            line = line.strip().split(delim)
            line = [float(i) for i in line]
            data.append(torch.tensor(line, dtype=torch.float32))
    return data